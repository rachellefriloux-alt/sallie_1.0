/**
 * Different accessibility levels for UI customization
 */
enum class AccessibilityLevel {
    /**
     * Standard presentation with default text sizes and animation speeds
     */
    STANDARD,
    
    /**
     * High contrast mode with bolder text and increased color contrast
     */
    HIGH_CONTRAST,
    
    /**
     * Reduced motion for users sensitive to animations
     */
    REDUCED_MOTION
}
